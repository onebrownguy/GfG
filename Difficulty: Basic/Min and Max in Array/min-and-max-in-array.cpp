class Solution {
  public:
    pair<int, int> getMinMax(vector<int> arr) {
        // code here
        int minNum = arr[0];
        int maxNum = arr[0];
       
        for (int i = 0; i < arr.size(); i++){
            if (arr[i] < minNum)
                minNum = arr[i];
            if (arr[i] > maxNum)
                maxNum = arr[i];
        }
        return {minNum, maxNum};
    }
};

